    public void OnClick_SearchBtn(View view) {
        mMap.clear();
        EditText Txt_SearchLocation = (EditText) findViewById(R.id.Txt_SearchLocation);
        String String_location = Txt_SearchLocation.getText().toString();
        List<Address> addressList = null;
        boolean didItWork = true;
        if (String_location != null || !String_location.equals("")) {
            Geocoder geocoder = new Geocoder(this);
            try {
                addressList = geocoder.getFromLocationName(String_location, 1);
            } catch (IOException e) {
                didItWork = false;
                e.printStackTrace();
            }

            Address address = addressList.get(0);
            LatLng latLng = new LatLng(address.getLatitude(), address.getLongitude());
            mMap.addMarker(new MarkerOptions().position(latLng).title("Searched Location"));
            mMap.animateCamera(CameraUpdateFactory.newLatLng(latLng));
        }
    }

